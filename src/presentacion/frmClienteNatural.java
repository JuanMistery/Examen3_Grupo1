    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Cliente ClienteB;
        String nombres = txtNombres.getText();
        String apellidos = txtApellidos.getText();
        int tipoDocumento = cmbTipoDocumento.getSelectedIndex()+1;
        String documento = txtDocumentoIdentidad.getText();
        int estadoCivil = cmbEstadoCivil.getSelectedIndex()+1;
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        String correoElectronico = txtCorreoElectronico.getText();
        int dia = cmbDia.getSelectedIndex();
        int mes = cmbMes.getSelectedIndex();
        int anio = Integer.parseInt(cmbAnio.getSelectedItem().toString());
        GregorianCalendar fechaNacimiento = new GregorianCalendar();
        fechaNacimiento.set(anio,mes,dia);
        ClienteB = new ClienteNatural(tipoDocumento,documento,estadoCivil,fechaNacimiento,nombres,apellidos,direccion,telefono,correoElectronico);
        cuentaB.setTitularDeCuenta(ClienteB);
        if(listaC.agregarCuenta(cuentaB))
        {
            if(listaC.guardarEnArchivo("DatosCuentas.dat"))
            {
                
            }
        }
        JOptionPane.showMessageDialog(null, "Registrado con exito:  \nSu numero de cuenta es: " + cuentaB.getNumeroCuenta(), "Cuenta no encontrada: ", JOptionPane.INFORMATION_MESSAGE);
        
        frmLogin FrmLogin = new frmLogin();
        FrmLogin.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

